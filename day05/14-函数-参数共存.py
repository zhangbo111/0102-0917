# 1、关键字参数必须放在所有形参之后
# 2、普通参数一般放在元组参数之前
# 3、元组参数必须按照固定的格式进行传参
# 4、在形参位置只能书写一个元组参数和一个关键字参数 普通参数不受限制
def fruit(str1, str2, *args, **kwargs):
    print("普通参数：", str1, str2)
    print("元组参数：", args)
    print("关键字参数：", kwargs)
# 普通参数接收完之后 所有的非关键字参数由args元组形参进行接收
fruit('香蕉', '柚子', '梨子', str5='苹果',str6='橘子')











