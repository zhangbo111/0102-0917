# 触发异常
def start():
    num = int(input("请输入一个大于3的数字："))
    try:
        if num < 3:
            # 自定义异常 触发异常
            raise Exception('您输入的数字太小了，小于3不符合规定')
    except Exception as e:
        print("异常描述信息：", e)

start()

