dict0 = {}
for k, v in zip(['千', '山','鸟','飞', '绝'], ['万','径','人','踪','灭']):
    dict0[k] = v
print(dict0)
# 字典推导式 可以减少代码的行数
dict1 = {k: v for k, v in zip(['千','山','鸟','飞', '绝'], ['万','径','人','踪','灭'])}
print(dict1)

# 集合推导式
lst = [1, 2, 3, 3, 4, 5, '李白', '李白']
# 集合推导式 是把列表中的元素 一个一个的放入集合中 重复的元素去掉
set1 = {i for i in lst}
print(set1)










