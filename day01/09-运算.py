# bool值运算 与运算
# 两边都是True返回True
result1 = True and True
print(result1)
# and两边只要有一个不为True 返回False
result2 = True and False
print(result2)
# 对比较运算做真假判断
result3 = 5 > 2 and 3 < 4
print(result3)
# 或运算 只要两边其中一个为True返回True
result4 = True or False
print(result4)
# 非运算 真变假 假变真
result5 = not True
print(result5)



