# upper把所有的单词变成大写
str1 = 'Where are you from'
result1 = str1.upper()
print(result1)
# lower把所有的单词变为小写
str2 = 'WHERE ARE YOU FROM'
result2 = str2.lower()
print(result2)
# 大小写互换 大写变小写 小写变大写
str3 = 'Where Are You From'
result3 = str3.swapcase()
print(result3)
# 把所有的单词的首字母变为大写
str4 = 'where are you from'
result4 = str4.title()
print(result4)
# 把整个字符串的第一个单词的首字母进行大写
# 其他的字母都是小写
str5 = 'where are you from'
result5 = str5.capitalize()
print(result5)


